#include "GoKart.h"
#include "Components/InputComponent.h"
#include "Engine/World.h"
#include "GoKartMovementComponent.h"
#include "GoKartMoveReplicationComponent.h"

// Sets default values
AGoKart::AGoKart()
{
 	// Set this pawn to call Tick() every frame.  You can turn this off to improve performance if you don't need it.
	PrimaryActorTick.bCanEverTick = true;

	bReplicates = true;

	goKartMovement = CreateDefaultSubobject<UGoKartMovementComponent>(TEXT("GoKartMovementComponent"));
	goKartMovementReplication = CreateDefaultSubobject<UGoKartMoveReplicationComponent>(TEXT("GoKartMoveReplicationComponent"));
}

// Called when the game starts or when spawned
void AGoKart::BeginPlay()
{
	Super::BeginPlay();

	if (HasAuthority())
	{
		NetUpdateFrequency = 1;
	}
}

// Called every frame
void AGoKart::Tick(float DeltaTime)
{
	Super::Tick(DeltaTime);
}

// Called to bind functionality to input
void AGoKart::SetupPlayerInputComponent(UInputComponent* PlayerInputComponent)
{
	Super::SetupPlayerInputComponent(PlayerInputComponent);
	PlayerInputComponent->BindAxis("MoveForward", this, &AGoKart::MoveForward);
	PlayerInputComponent->BindAxis("MoveRight", this, &AGoKart::MoveRight);
}


void AGoKart::MoveForward(float value)
{
	goKartMovement->SetThrottle(value);
}

void AGoKart::MoveRight(float value)
{
	goKartMovement->SetSteeringThrow(value);
}
